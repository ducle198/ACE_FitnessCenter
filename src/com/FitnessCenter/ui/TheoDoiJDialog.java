    private void update(String a) {
        TheoDoi model = getModel();
        try {
            dao.Update(model);
            this.load(a);
            DialogHelper.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại!");
            e.printStackTrace();
        }
    }
